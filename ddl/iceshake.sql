ALTER PROCEDURE ADDPNGIMAGE (IMAGE D_IMAGE,
AID D_ID,
OVERWRITE D_BOOLEAN = false)
AS 
declare variable FK_IMAGE D_FK;
declare variable ID D_FK;
begin
  /* Procedure Text */
  FK_IMAGE = null;
  select A.FK_IMAGE
  from ARTISTS A
  where A.ID = :AID
  into :FK_IMAGE;
  if (FK_IMAGE is null) then
  begin
    insert into IMAGES (source, IMAGE, MIMETYPE)
    values ('' ,:IMAGE, 'png')
    returning ID
    into :ID;
    update artists a set a.fk_image=:ID where a.id=:aid;
  end
  else
  if (:overwrite) then
  begin
    update images i set i.image=:image where i.id=:fk_image;
  end
end ^

ALTER PROCEDURE ALBUMLIST (NAME VARCHAR(128) CHARACTER SET UTF8)
AS 
declare variable ID D_FK;
declare variable FK_TRACK D_FK;

begin
     delete from wl;
     for select row_number() over (order by atr.number),  atr.id from  tracks atr join albums a  on atr.album_pk =a.id where a.name=:NAME into :id,:fk_track
     do
       insert into wl (id,fk_track) values (:id,:fk_track);
    update actpos set wp=0,ap=ap+1;
  end ^

