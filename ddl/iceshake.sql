ALTER PROCEDURE TOCSV RETURNS (CSVOUT TYPE OF D_VC1024)
AS 
begin
for select '"'||A.NAME||'";"'||T.TITLE||'";"'||coalesce(AL.NAME,'unknown')||'";"'||substring(T.PATH from 20)||'"'
from
    TRACKS T
join
    ARTISTS A on T.ARTIST_PK = A.ID
left join
    ALBUMS AL on T.ALBUM_PK = AL.ID
--where A.name='Hozier'
order by
    a.name, coalesce(al.name,'unknown'),t.number,  t.title
    into :CSVout do
  suspend;
end ^

