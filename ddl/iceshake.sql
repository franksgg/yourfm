ALTER FUNCTION ACTNAME RETURNS D_VC256
AS 
begin
  return (select
              coalesce(AL.NAME, 'unknown') || ' (' || coalesce(T.PYEAR, 'unknown')||') - ' || coalesce(A.NAME, 'unknown') || ' - ' || coalesce(T.TITLE, 'unknown')
          from TRACKS T
          join
              ARTISTS A on T.ARTIST_PK = A.ID
          join
              ALBUMS AL on al.ID = T.ALBUM_PK
          where
              T.ID = TNR(1));
end ^

